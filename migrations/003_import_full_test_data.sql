-- 4. 计算出芽率统计 (Germination Statistics)
-- 从生长日志中汇总数据并插入到germination_stats表
INSERT OR REPLACE INTO germination_stats (batch, plant_short_name, seeds_sown, seeds_germinated, seeds_dead, notes)
SELECT
    '第一批' as batch,
    p.short_name,
    -- 播种数：统计播种事件的数量
    COALESCE((
        SELECT COUNT(*)
        FROM growth_log_batch1 g
        WHERE g.plant_short_name = p.short_name
        AND g.event_type = '播种'
        AND g.quantity_location LIKE '%粒%'
    ), 0) as seeds_sown,
    -- 已出芽：统计出芽事件的数量（简化：按事件计数）
    COALESCE((
        SELECT COUNT(*)
        FROM growth_log_batch1 g
        WHERE g.plant_short_name = p.short_name
        AND g.event_type = '出芽'
    ), 0) as seeds_germinated,
    -- 已死亡：统计死亡事件的数量
    COALESCE((
        SELECT COUNT(*)
        FROM growth_log_batch1 g
        WHERE g.plant_short_name = p.short_name
        AND g.event_type = '死亡'
    ), 0) as seeds_dead,
    -- 备注
    '自动计算' as notes
FROM plant_archive p
WHERE p.short_name IN ('线椒', '樟树', '朝天', '灯笼', '番茄1', '番茄2', '番茄3', '茄子', '土豆', '黄瓜')
UNION ALL
SELECT
    '第二批' as batch,
    p.short_name,
    COALESCE((
        SELECT COUNT(*)
        FROM growth_log_batch2 g
        WHERE g.plant_short_name = p.short_name
        AND g.event_type = '播种'
        AND g.quantity_location LIKE '%粒%'
    ), 0) as seeds_sown,
    COALESCE((
        SELECT COUNT(*)
        FROM growth_log_batch2 g
        WHERE g.plant_short_name = p.short_name
        AND g.event_type = '出芽'
    ), 0) as seeds_germinated,
    0 as seeds_dead,  -- 第二批暂无死亡记录
    '自动计算' as notes
FROM plant_archive p
WHERE p.short_name IN ('南瓜', '西瓜', '甜瓜', '洛神', '秋葵', '苦瓜', '丝瓜', '西葫');

-- 5. 更新计算字段（出芽率和活苗率）
UPDATE germination_stats
SET
    seeds_pending = seeds_sown - seeds_germinated - seeds_dead,
    germination_rate = CASE
        WHEN seeds_sown > 0 THEN ROUND((seeds_germinated * 100.0 / seeds_sown), 2)
        ELSE 0
    END,
    survival_rate = CASE
        WHEN seeds_sown > 0 THEN ROUND(((seeds_germinated - seeds_dead) * 100.0 / seeds_sown), 2)
        ELSE 0
    END,
    calculated_at = CURRENT_TIMESTAMP;

